function redirect(v) {
    if (v == "score") {
        window.location.href = "getscore.html";
    } else if (v == "courseTable") {
        window.location.href = "ClassTable.html";
    } else if (v == "emptyClassroom") {
        window.location.href = "EmptyClassRoom.html";
    } else if (v == "chineseQuery") {
        window.location.href = "chinesequery.html";
    } else if (v == "bookQuery") {
        window.location.href = "bookquery.html";
    } else if (v == "ideaclick") {
        window.location.href = "idea.html";
    } else if (v == "cet46") {
        window.location.href = "CET64.html";
    }

}
